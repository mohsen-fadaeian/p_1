    public function update(Request $request, $id)
    {

        if ($request->password == '')
            {
                $a = $request->except('password');
            }
        else
            {
                $a= $request->all();
                $a['password'] = bcrypt($request->password);
            }
        $b = User::findOrfail($id);
        $b->update($a);
        return redirect('admin/students_login');
